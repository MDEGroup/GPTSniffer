package ch_05;

import java.util.*;


public class Exercise05_23 {
    public static void main(String[] args) {
        double checkSum = 0.0;
        for (int i = 1000; i >= 1; i--) {
            checkSum += 1.0 / i;
        }
        System.out.println("Result of computing sum of the series from right to left was " + checkSum);


    }
}