package ch_07;


public class Exercise07_07 {
    public static void main(String[] args) {
        int[] counts = new int[10];
        int randomNum = 0;
        for (int i = 0; i < 100; i++) {
            randomNum = (int) (Math.random() * 10);
            counts[randomNum]++;
        }

        for (int i = 0; i < counts.length; i++) {
            System.out.print(counts[i] + " counts of ");
            System.out.println(i);
        }


    }

}
